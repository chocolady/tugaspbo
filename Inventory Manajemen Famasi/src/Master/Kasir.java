    private void prosesData(String proses){
    String value;
//    int row= tblBarang.getSelectedRow();
    switch (proses) {
            case "insert":
                        value="'"+tID.getText()+"','"+tNama.getText()+"','"+tAlamat.getText()+"','"+tTlp.getText()+"'";
                        System.out.println(value);
                        new Koneksi().ExecuteSQl(proses, "t_kasir", "id_kasir,nama,alamat,no_tlp", value);
                        JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
                 break;    
                case "update":
                value = "nama = '"+ tNama.getText() +"',";                
                value += "alamat = '"+ tAlamat.getText()+"',";
                value += "no_tlp = '"+ tTlp.getText() +"'";
                
                new Koneksi().ExecuteSQl(proses, "t_kasir", value, "id_kasir = '"+tID.getText()+"'");
                JOptionPane.showMessageDialog(this, "Data berhasil di update");
                break;
                case "delete":
                int i = tKasir.getSelectedRow();
                String data = tKasir.getValueAt(i, 0).toString();

                new Koneksi().ExecuteSQl(proses, "t_kasir", "", "id_kasir ='"+data+"'");
                JOptionPane.showMessageDialog(this, "Data berhasil di hapus");
                break;
    }
}
