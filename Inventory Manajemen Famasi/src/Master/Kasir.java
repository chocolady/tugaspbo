    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jButton1.getText().equals("Tambah")){
            Enable(true);
            jButton1.setText("Simpan");
            jButton3.setText("Batal");
            jButton2.setEnabled(false);
        }else{
            if(tNama.getText().trim().isEmpty()||tAlamat.getText().trim().isEmpty()
                ||tTlp.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "Mohon lengkapi semua field");
            }else if(jButton1.getText().equals("Simpan") && jButton2.getText().equals("Ubah")){
                prosesData("insert");
                jButton1.setText("Tambah");
                jButton3.setText("Hapus");
                showTab();
                clear();
                Enable(false);
                jButton2.setEnabled(true);
            }else if(jButton2.getText().equals("Batal")){
                prosesData("update");
                showTab();
                clear();
                Enable(false);
                jButton2.setText("Ubah");
                jButton3.setEnabled(true);
                jButton1.setText("Tambah");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jButton2.getText().equals("Ubah")) {
            if (tKasir.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(this, "Anda belum memilih data untuk diubah");
            } else {
                jButton2.setText("Batal");
                jButton1.setText("Simpan");
                jButton3.setEnabled(false);
                Enable(true);
                tID.setEnabled(false);
                int row = tKasir.getSelectedRow();
                tID.setText(tKasir.getValueAt(row, 0).toString());
                tNama.setText(tKasir.getValueAt(row, 1).toString());
                tAlamat.setText(tKasir.getValueAt(row, 2).toString());
                tTlp.setText(tKasir.getValueAt(row, 3).toString());
            }
        } else {
            clear();
            Enable(false);
            jButton1.setText("Tambah");
            jButton2.setText("Ubah");
            jButton3.setEnabled(true);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jButton3.getText().equals("Batal")) {
            clear();
            Enable(false);
            jButton3.setText("Hapus");
            jButton1.setText("Tambah");
            jButton2.setEnabled(true);
        } else {
            if (tKasir.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(this, "Pilih data yang akan di hapus");
            } else {
                prosesData("delete");
                showTab();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed


